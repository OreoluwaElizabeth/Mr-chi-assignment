public class DecreasingTriangle {

    public static void main(String [] args) {
  
        int rows = 5;

        for (int i = rows; i > 0; i--) {
            for (int j = i; j > 0; j--) {
                System.out.print(j + " ");
                    }
            System.out.println();
                  }
 
                 }
               }